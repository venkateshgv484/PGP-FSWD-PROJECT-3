package com.simplilearn.sportyshoes.entity;

public class Category {

	private long ID;
	private String name;

	public long getID() {
		return this.ID;
	}

	public String getName() {
		return this.name;
	}

	public void setID(long id) {
		this.ID = id;
	}

	public void setName(String value) {
		this.name = value;
	}

}
